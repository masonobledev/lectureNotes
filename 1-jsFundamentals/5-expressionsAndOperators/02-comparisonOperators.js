/*
COMPARISON OPERATORS

*/

//equality comparison operator
console.log('3' == 3);
console.log(4 == 4);
console.log(3 == '4');

//strict equality comparison operator
console.log(3 === 3);
console.log('3' === 3);

//not equal comparison operator  (opposite of the equality operator)
console.log('3' != 4);
console.log('3' != 3);

//strict not equal comparison operator
console.log('3' !== 3);
console.log(3 !== 3);

//greater than
console.log(3 > 2);
console.log(2 > 3);

//less than 
console.log(2 < 3);
console.log(3 < 2);

//greater than or equal to
console.log(3 >= 2);
console.log(3 <= 2);

